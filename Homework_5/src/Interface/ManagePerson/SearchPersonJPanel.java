    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        ArrayList<Person> searchPersonList = new ArrayList<>();
        
        if (searchFirstNameTxt.getText().equals("") && searchLastNameTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Please fill in at least one blank.","Alert",JOptionPane.WARNING_MESSAGE);
            return;
        }
            else if (searchFirstNameTxt.getText().equals("")) {
            for (Person person: business.getPersonDir().getPersonList()) {
                if (person.getLastName().equals(searchLastNameTxt.getText())) {
                    searchPersonList.add(person);
                }
            }
        }
            //person = business.getPersonDir().findPersonByLastName(searchLastNameTxt.getText());
        else if (searchLastNameTxt.getText().equals("")) {
            for (Person person: business.getPersonDir().getPersonList()) {
                if (person.getFirstName().equals(searchFirstNameTxt.getText())) {
                    searchPersonList.add(person);
                }
            }
        }
        else {
            for (Person person: business.getPersonDir().getPersonList()) {
                if (person.getLastName().equals(searchLastNameTxt.getText()) && 
                        person.getFirstName().equals(searchFirstNameTxt.getText())) {
                    searchPersonList.add(person);
                }
            }
        }
        
        if (searchPersonList.size() != 0) {
            ResultPersonJPanel panel = new ResultPersonJPanel(userProcessContainer, business, searchPersonList);
            userProcessContainer.add("ViewPersonJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
            JOptionPane.showMessageDialog(null,"No Person found.","Alert",JOptionPane.WARNING_MESSAGE); 
    }//GEN-LAST:event_searchBtnActionPerformed


