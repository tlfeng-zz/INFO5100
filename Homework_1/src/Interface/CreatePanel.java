    private void uploadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadBtnActionPerformed
        // Open File Chooser
        JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(null);
        switch (result) {
        case JFileChooser.APPROVE_OPTION:
            //System.out.println("Approve (Open or Save) was clicked");
            File imgFile = chooser.getSelectedFile();
            String imgPath = imgFile.getAbsolutePath();
            //Read the image as bufferedimage
            BufferedImage img = null;
            try {
                img = ImageIO.read(new File(imgPath));
            } catch (IOException e) {
                e.printStackTrace();
            }
            //Resize the BufferedImage
            Image dimg = img.getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(),
            Image.SCALE_SMOOTH);
            //Display person portrait in appropriate size
            imageLabel.setIcon(new javax.swing.ImageIcon(dimg)); 
            break;
        case JFileChooser.CANCEL_OPTION:
            //System.out.println("Cancel or the close-dialog icon was clicked");
            break;
        case JFileChooser.ERROR_OPTION:
            //System.out.println("Error");
            break;
    }
    }//GEN-LAST:event_uploadBtnActionPerformed

    private void savePrimaryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePrimaryButtonActionPerformed
        // Binding the user input to the person object
        person.setFirstName(firstNameTextField.getText());
        person.setLastName(lastNameTextField.getText());
        person.setGender((String)genderComboBox.getSelectedItem());
        person.setDobMonth((String)genderComboBox.getSelectedItem());
        person.setDobDay(Integer.parseInt(dobDayTextField.getText()));
        person.setDobYear(Integer.parseInt(dobYearTextField.getText()));
        person.setSsn(Integer.parseInt(ssnTextField.getText()));
        
        // Prompt user that person was successfully created.
        JOptionPane.showMessageDialog(null,"Create Primary Profile Successfully");
    }//GEN-LAST:event_savePrimaryButtonActionPerformed

