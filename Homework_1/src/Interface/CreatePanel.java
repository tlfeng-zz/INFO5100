    private void saveFinancialBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFinancialBtnActionPerformed
        // Judge RadioButton
        boolean status;
        if(buttonGroup1.getSelection().equals(fARadioButton.getModel()))
            status = true;
        else
            status = false;

        // Binding the user input to the financial accounts object
        person.financialAcc.setAccountNum(Integer.parseInt(fAccNumTextField.getText()));
        person.financialAcc.setCreationDate(fCDateTextField.getText());
        person.financialAcc.setStatus(status);
        person.financialAcc.setDebt(Integer.parseInt(fDebtTextField.getText()));
        person.financialAcc.setCredit(Integer.parseInt(fCreditTextField.getText()));
        // Prompt user that financial accounts were successfully created.
        JOptionPane.showMessageDialog(null,"Create Financial Accounts Data Successfully");
    }//GEN-LAST:event_saveFinancialBtnActionPerformed

