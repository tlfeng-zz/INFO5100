package Business;

/**
 *
 * @author Tianli Feng
 */
public class Airplanes {
    private int serialNum;
    private String modelNum;
    private String menufacture;
    private String menufDate;
    private int seatCapacity;
    private String maintainDate;
    private String availDate;
    private String airport;

    public int getSerialNum() {
        return serialNum;
    }

    public void setSerialNum(int serialNum) {
        this.serialNum = serialNum;
    }

    public String getModelNum() {
        return modelNum;
    }

    public void setModelNum(String modelNum) {
        this.modelNum = modelNum;
    }

    public String getMenufacture() {
        return menufacture;
    }

    public void setMenufacture(String menufacture) {
        this.menufacture = menufacture;
    }

    public String getMenufDate() {
        return menufDate;
    }

    public void setMenufDate(String menufDate) {
        this.menufDate = menufDate;
    }

    public int getSeatCapacity() {
        return seatCapacity;
    }

    public void setSeatCapacity(int seatCapacity) {
        this.seatCapacity = seatCapacity;
    }

    public String getMaintainDate() {
        return maintainDate;
    }

    public void setMaintainDate(String maintainDate) {
        this.maintainDate = maintainDate;
    }

    public String getAvailDate() {
        return availDate;
    }

    public void setAvailDate(String availDate) {
        this.availDate = availDate;
    }

    public String getAirport() {
        return airport;
    }

    public void setAirport(String airport) {
        this.airport = airport;
    }
    
    
}
